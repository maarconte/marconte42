#include <unistd.h>
void	ft_putchar(char c)
{
	write(1, &c,1);
}

int		main(int ac, char **av)
{
	int i;

	i = 0;
	if (ac >= 2)
		if (*av[i] != " ")
			ft_putchar(*av[i++]);
	return (0);
}
